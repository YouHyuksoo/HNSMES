        private void GetGridViewList(string p_sData)
        {
            BASE_DXGridHelper.Bind_Grid_Int( gcList
                                           , "PKGPRD_REPORT.GET_WO_DETAIL_HIST"
                                           , 1
                                           , new string[] { 
                                             "A_CLIENT"
                                           , "A_COMPANY"
                                           , "A_PLANT"
                                           , "A_BARCODE" }
