        #region 일반이벤트
        
        private void txtSN_KeyDown(object sender, KeyEventArgs e)
        {
            if (e.KeyCode == Keys.Enter)
            {
                if (txtSN.Text.Trim() == "")
                    return;

                this.Get_BarcodeType(txtSN.Text);

                txtSN.Text = "";
                txtSN.Focus();
            }
        }

